/** 検索用にフラット化した全記事（カテゴリ情報付き） */
export interface FlatArticle {
  categorySlug: string
  categoryTitle: string
  audience: HelpAudience
  id: string
  q: string
  /** 検索対象テキスト（質問＋回答＋手順を連結） */
  text: string
}

export function flattenArticles(): FlatArticle[] {
  const out: FlatArticle[] = []
  for (const c of HELP_CATEGORIES) {
    for (const a of c.articles) {
      out.push({
        categorySlug: c.slug,
        categoryTitle: c.title,
        audience: c.audience,
        id: a.id,
        q: a.q,
        text: [a.q, ...a.a, ...(a.steps ?? []), a.note ?? ''].join(' '),
      })
    }
  }
  return out
}
